package com.expeditors.domain;

import org.springframework.lang.NonNull;


public class Artist {
    private int id;
    @NonNull
    private String name;

    public Artist(){}

    public Artist(String name){
        setName(name);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Artist{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
